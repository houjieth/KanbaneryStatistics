package net.jiehou.tools;

public class SumsTableEntry {
	public float sumAll;
	public float sumS;
	public float sumT;
	public float sumP;
	public float sumI;
	public float sumO;
	public float sumM;
	public float sumMM;
	public float sumGW;
	public float sumLI;
	public float sumBC;
	public float sumSA;
	public float sumRM;
	
	public SumsTableEntry(float sumAll, float sumS, float sumT, float sumP,
			float sumI, float sumO, float sumM, float sumMM, float sumGW,
			float sumLI, float sumBC, float sumSA, float sumRM) {
		super();
		this.sumAll = sumAll;
		this.sumS = sumS;
		this.sumT = sumT;
		this.sumP = sumP;
		this.sumI = sumI;
		this.sumO = sumO;
		this.sumM = sumM;
		this.sumMM = sumMM;
		this.sumGW = sumGW;
		this.sumLI = sumLI;
		this.sumBC = sumBC;
		this.sumSA = sumSA;
		this.sumRM = sumRM;
	}
}
